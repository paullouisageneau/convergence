vec3 Surface::Block::center(void) const
{
	return vec3((float(mPos.x) + 0.5f)*Size,
			(float(mPos.y) + 0.5f)*Size,
			(float(mPos.z) + 0.5f)*Size);
}

Surface::value Surface::Block::getValue(const int3 &c)
{
	if(c.x >= 0 && c.y >= 0 && c.z >= 0 && c.x < Size && c.y < Size && c.z < Size)
	{
		return readValue(c);
	} else {
		int3 p = fullCoord(mPos, c);
		sptr<Block> block = mRetrieveFunc(blockCoord(p));
		return block->readValue(cellCoord(p));
	}
}

int84 Surface::Block::getGradient(const int3 &c)
{
	if(c.x >= 0 && c.y >= 0 && c.z >= 0 && c.x < Size && c.y < Size && c.z < Size)
	{
		return computeGradient(c);
	} else {
		int3 p = fullCoord(mPos, c);
		sptr<Block> block = mRetrieveFunc(blockCoord(p));
		return block->computeGradient(cellCoord(p));
	}
}

int Surface::Block::update(void)
{
	if(!hasChanged()) return indicesCount()/3;

	std::vector<vec3> vertices;
	std::vector<int84> normals;
	std::vector<int84> materials;
	std::vector<index_t> indices;

	vertices.reserve(vertexAttribCount()*2);
	normals.reserve(vertexAttribCount()*2);
	materials.reserve(vertexAttribCount()*2);
	indices.reserve(indicesCount()*2);

	for(int x = 0; x < Size; ++x)
		for(int y = 0; y < Size; ++y)
			for(int z = 0; z < Size; ++z)
				polygonizeCell(int3(x, y, z), vertices, normals, materials, indices);

	setIndices(&indices[0], indices.size());
	setVertexAttrib(0, glm::value_ptr(vertices[0]), vertices.size()*3, 3, false);
	setVertexAttrib(1, reinterpret_cast<char*>(&normals[0]), normals.size()*4, 4, true);
	setVertexAttrib(2, reinterpret_cast<char*>(&materials[0]), materials.size()*4, 4, true);

	optimize();
	return indicesCount()/3;
}

// Given a grid cell, calculate the triangular faces required to represent the surface through the cell.
// Fill indices and attributes arrays, and return the number of faces generated.
int Surface::Block::polygonizeCell(const int3 &c,
	std::vector<vec3> &vertices, std::vector<int84> &normals, std::vector<int84> &materials,
	std::vector<index_t> &indices)
{
	vec3 center = vec3(	float(mPos.x*Size+c.x),
				float(mPos.y*Size+c.y),
				float(mPos.z*Size+c.z));	// cell center

	vec3 p[8];	// grid vertices
	p[0] = center + vec3(-.5f, -.5f, -.5f);
	p[1] = center + vec3( .5f, -.5f, -.5f);
	p[2] = center + vec3( .5f,  .5f, -.5f);
	p[3] = center + vec3(-.5f,  .5f, -.5f);
	p[4] = center + vec3(-.5f, -.5f,  .5f);
	p[5] = center + vec3( .5f, -.5f,  .5f);
	p[6] = center + vec3( .5f,  .5f,  .5f);
	p[7] = center + vec3(-.5f,  .5f,  .5f);

	value v[8];	// grid values
	v[0] = getValue(int3(c.x-1, c.y-1, c.z-1));
	v[1] = getValue(int3(c.x  , c.y-1, c.z-1));
	v[2] = getValue(int3(c.x  , c.y  , c.z-1));
	v[3] = getValue(int3(c.x-1, c.y  , c.z-1));
	v[4] = getValue(int3(c.x-1, c.y-1, c.z  ));
	v[5] = getValue(int3(c.x  , c.y-1, c.z  ));
	v[6] = getValue(int3(c.x  , c.y  , c.z  ));
	v[7] = getValue(int3(c.x-1, c.y  , c.z  ));

	int84 g[8];	// grid gradients
	g[0] = getGradient(int3(c.x-1, c.y-1, c.z-1));
	g[1] = getGradient(int3(c.x  , c.y-1, c.z-1));
	g[2] = getGradient(int3(c.x  , c.y  , c.z-1));
	g[3] = getGradient(int3(c.x-1, c.y  , c.z-1));
	g[4] = getGradient(int3(c.x-1, c.y-1, c.z  ));
	g[5] = getGradient(int3(c.x  , c.y-1, c.z  ));
	g[6] = getGradient(int3(c.x  , c.y  , c.z  ));
	g[7] = getGradient(int3(c.x-1, c.y  , c.z  ));

	// Determine the index into the edge table
	unsigned index = 0;
	if(v[0].weight == 0) index|= 0x01;
	if(v[1].weight == 0) index|= 0x02;
	if(v[2].weight == 0) index|= 0x04;
	if(v[3].weight == 0) index|= 0x08;
	if(v[4].weight == 0) index|= 0x10;
	if(v[5].weight == 0) index|= 0x20;
	if(v[6].weight == 0) index|= 0x40;
	if(v[7].weight == 0) index|= 0x80;

	unsigned edge = EdgeTable[index];

	// Cube is entirely in/out of the surface
	if(edge == 0) return 0;

	// Find the vertices, gradients and materials where the surface intersects the cube
	vec3 vert[12];
	int84 grad[12];
	int84 mat[12];

	if(edge & 0x001) vert[0]  = interpolate(p[0], p[1], g[0], g[1], v[0], v[1], grad[0],  mat[0]);
	if(edge & 0x002) vert[1]  = interpolate(p[1], p[2], g[1], g[2], v[1], v[2], grad[1],  mat[1]);
	if(edge & 0x004) vert[2]  = interpolate(p[2], p[3], g[2], g[3], v[2], v[3], grad[2],  mat[2]);
	if(edge & 0x008) vert[3]  = interpolate(p[3], p[0], g[3], g[0], v[3], v[0], grad[3],  mat[3]);
	if(edge & 0x010) vert[4]  = interpolate(p[4], p[5], g[4], g[5], v[4], v[5], grad[4],  mat[4]);
	if(edge & 0x020) vert[5]  = interpolate(p[5], p[6], g[5], g[6], v[5], v[6], grad[5],  mat[5]);
	if(edge & 0x040) vert[6]  = interpolate(p[6], p[7], g[6], g[7], v[6], v[7], grad[6],  mat[6]);
	if(edge & 0x080) vert[7]  = interpolate(p[7], p[4], g[7], g[4], v[7], v[4], grad[7],  mat[7]);
	if(edge & 0x100) vert[8]  = interpolate(p[0], p[4], g[0], g[4], v[0], v[4], grad[8],  mat[8]);
	if(edge & 0x200) vert[9]  = interpolate(p[1], p[5], g[1], g[5], v[1], v[5], grad[9],  mat[9]);
	if(edge & 0x400) vert[10] = interpolate(p[2], p[6], g[2], g[6], v[2], v[6], grad[10], mat[10]);
	if(edge & 0x800) vert[11] = interpolate(p[3], p[7], g[3], g[7], v[3], v[7], grad[11], mat[11]);

	// Create the triangles
	int n = 0;
	std::map<int, index_t> mapping;
	for(int i = 0; TriTable[index][i] >= 0; i+= 3)	// for each triangle
	{
		for(int j = 0; j < 3; ++j)	// for each vertex
		{
			int vi = TriTable[index][i+j];
			auto it = mapping.find(vi);
			if(it == mapping.end())
			{
				index_t mi = index_t(vertices.size());
				vertices.push_back(vert[vi]);
				normals.push_back(-grad[vi].normalize());
				materials.push_back(mat[vi]);
				indices.push_back(mi);
				mapping[vi] = mi;
			}
			else {
				indices.push_back(it->second);
			}
		}

		++n;
	}

	return n;
}

// Linearly interpolate position, gradient, and material where the surface intersects an edge.
vec3 Surface::Block::interpolate(vec3 p1, vec3 p2, int84 g1, int84 g2, value v1, value v2, int84 &grad, int84 &mat)
{
	static auto comp = [](const vec3 &a, const vec3 &b)
	{
		if (a.x < b.y) return true;
		else if (a.x > b.x) return false;
		if (a.y < b.y) return true;
		else if (a.y > b.y) return false;
		if (a.z < b.z) return true;
		else return false;
	};

	if(comp(p1, p2))
	{
		std::swap(p1, p2);
		std::swap(g1, g2);
		std::swap(v1, v2);
	}

	float mu = v1.weight == 0 ? 1.f - v2.w() : v1.w();

	vec4 grad1(g1);
	vec4 grad2(g2);
	grad = int84(grad1 + (grad2 - grad1) * mu);

	vec4 mat1 = (v1.type < 4 ? MaterialTable[v1.type] * 127.f : vec4(0.f, 0.f, 0.f, 0.f));
	vec4 mat2 = (v2.type < 4 ? MaterialTable[v2.type] * 127.f : vec4(0.f, 0.f, 0.f, 0.f));
	mat = int84(mat1 + (mat2 - mat1) * mu);

	return p1 + (p2 - p1)*mu;
}

int84 Surface::Block::computeGradient(const int3 &p)
{
	return int84(
	    (getValue(int3(p.x + 1, p.y, p.z)).weight - getValue(int3(p.x - 1, p.y, p.z)).weight) / 2,
	    (getValue(int3(p.x, p.y + 1, p.z)).weight - getValue(int3(p.x, p.y - 1, p.z)).weight) / 2,
	    (getValue(int3(p.x, p.y, p.z + 1)).weight - getValue(int3(p.x, p.y, p.z - 1)).weight) / 2);
}

